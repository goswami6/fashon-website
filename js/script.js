
    // Function to toggle the visibility of the mobile navbar
    function toggleNavbar() {
        var navbar = document.querySelector('.navbar');
        navbar.style.display = (navbar.style.display === 'block' || navbar.style.display === '') ? 'none' : 'block';
    }

    // Event listener for the menu icon
    document.getElementById('menu-bar').addEventListener('click', toggleNavbar);




